#Create a program that ask user to input 2 numbers. Print "Equal" when the numbers are the same.

input1 = int(input("Please input a number: "))

input2 = int(input("Please input another number: "))

if input1 == input2:
    print(f"{input1} and {input2} is equal, yehey! :]")

else:
    print(f"{input1} and {input2} is not equal po! :[")